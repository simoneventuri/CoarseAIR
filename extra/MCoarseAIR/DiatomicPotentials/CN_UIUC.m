    VRef       = 0.0;%0.1915103559;
    BToAng     = 0.52917721067d0;
    EhToeV     = 27.2113839712790;

    re = 2.2285929293e+00;
    De = 7.5246407707e+00;
    PolyOrder = 15;  
    cPol = [ 1.3512029342e+00, 1.1452952601e-01,  3.9562643807e-01,  3.4232295049e-01, -3.2919084219e+00, ...
          -3.3691374231e+00, 1.9116673551e+01,  2.1060545371e+01, -5.3238093418e+01, -5.8945247450e+01, ...
           7.6574526682e+01, 7.9348242167e+01, -4.8801956192e+01, -3.9486044898e+01,  9.6418471372e+00, 0.0000000000e+00];

    RTemp4 =  r.^4;
    re4    = re.^4;
    y      = (RTemp4-re4) ./ (RTemp4+re4);
    y2     = y   .* y;
    y3     = y2  .* y;
    y4     = y3  .* y;
    y5     = y4  .* y;
    y6     = y5  .* y;
    y7     = y6  .* y;
    y8     = y7  .* y;
    y9     = y8  .* y;
    y10    = y9  .* y;
    y11    = y10 .* y;
    y12    = y11 .* y;
    y13    = y12 .* y;
    y14    = y13 .* y;

    poly   =    cPol(1)       + ... 
                cPol(2).*y    + ... 
                cPol(3).*y2   + ... 
                cPol(4).*y3   + ... 
                cPol(5).*y4   + ... 
                cPol(6).*y5   + ...
                cPol(7).*y6   + ... 
                cPol(8).*y7   + ... 
                cPol(9).*y8   + ... 
                cPol(10).*y9  + ... 
                cPol(11).*y10 + ...
                cPol(12).*y11 + ...
                cPol(13).*y12 + ...
                cPol(14).*y13 + ...
                cPol(15).*y14;

    dpoly  =        cPol(2)       + ...
                2.0*cPol(3).*y    + ...
                3.0*cPol(4).*y2   + ...
                4.0*cPol(5).*y3   + ...
                5.0*cPol(6).*y4   + ...
                6.0*cPol(7).*y5   + ...
                7.0*cPol(8).*y6   + ...
                8.0*cPol(9).*y7   + ...
                9.0*cPol(10).*y8  + ...
               10.0*cPol(11).*y9  + ...
              11.d0*cPol(12).*y10 + ...
              12.d0*cPol(13).*y11 + ...
              13.d0*cPol(14).*y12 + ...
              14.d0*cPol(15).*y13;


    dydR    =  4.d0 .* r.^3 .* ((RTemp4+re4)-(RTemp4-re4))./(RTemp4+re4).^2;

    V       =         De .* (1.d0 - exp(-poly .* (r-re))).^2  - De + cPol(16);
    dV      = 2.d0 .* De .* (1.d0 - exp(-poly .* (r-re))) .* (-exp(-poly  .* (r-re))) .* (-dpoly .* dydR .* (r-re) - poly);

    %V       = V  ./ EhToeV;
    %dV      = dV ./ EhToeV;

end